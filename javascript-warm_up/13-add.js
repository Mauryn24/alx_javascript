#!/usr/bin/bash/node

/*
    exports is an object in Node.js that allows you to export variables, functions, or objects to be used in other modules or scripts.

    add is the name of the function being exported.

    (a, b) => a + b is an arrow function. It takes two parameters a and b, and it returns the result of adding a and b together. In other words, it's a concise way of writing a function that adds two numbers.
*/
exports.add = (a, b) => a + b;